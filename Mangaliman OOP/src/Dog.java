
public class Dog {
	private String name;
	private String breed;
	
	public String getName() {
		return name;
	}
	
	public void setName(String newName) {
		this.name = newName;
	}
	
	public String getBreed() {
		return breed;
	}
	
	public void setBreed(String newBreed) {
		this.breed = newBreed;
	}
	
}
